class Attachment extends Model
{
    use SoftDeletes;

    public $timestamps = true;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'hash_name', 'assumed_ext', 'original_name', 'original_ext', 'persistent', 'file_module',
    ];

    
}
